#reverse for loop:

#Qs:1 print list in reverse
#reversed function

fruits = ["Mango", "Banana", "Guava", "Papaya", "Pomegranate"]

for fruit in reversed(fruits):
    print(fruit)

'''
Pomegranate
Papaya
Guava
Banana
Mango
'''

